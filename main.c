int main(void) {
  /* Declare volatile pointers to I/O registers (volatile means that IO load and
   * store instructions will be used to access these pointer locations instead
   * of regular memory loads and stores) */
  volatile int *interval_timer_ptr =
      (int *)0xFF202000;                      // interval timer base address
  volatile int *KEY_ptr = (int *)0xFF200050;  // pushbutton KEY address

  NIOS2_WRITE_IENABLE(0x3); /* set interrupt mask bits for levels 0 (interval
                             * timer) and level 1 (pushbuttons) */
  NIOS2_WRITE_STATUS(1);    // enable Nios II interrupts

  /* set the interval timer period for scrolling the HEX displays */
  *(interval_timer_ptr + 0x2) = (counter & 0xFFFF);
  /* start interval timer, enable its interrupts */
  *(interval_timer_ptr + 1) = 0x7;  // STOP = 0, START = 1, CONT = 1, ITO = 1
  countdown = 6;
  countdown_active = true;

  volatile int *pixel_ctrl_ptr = (int *)0xFF203020;
  pixel_buffer_start = *pixel_ctrl_ptr;
  volatile unsigned char pressedKey;

  // Clear the screen with white color
  clear_screen(BACKGROUND_COLOR);

  // RANDOMIZATION OF CARDS
  srand((unsigned int)time(NULL));  // seed with time

  for (int i = 0; i < 8; i++) {
    // assign a graphic to each card
    j = rand() % (8 - i);
    cards[i] = graphics[j];
    // remove the value we just used & shift the rest of the items down
    if (j != 7) {
      for (int k = j; k < (8 - i); k++) {
        graphics[k] = graphics[k + 1];
      }
    }
  }

  // INITIAL SET UP OF THE CARDS

  // Draw the cards face up for memorization
  for (int i = 0; i < 8; i++) {
    draw_card(i + 1, cards[i]);
  }

  // while (!gameStarted);  // idle here until timer is done and cards are
  // flipped

  // turn on interrupts for KEYS
  *(KEY_ptr + 2) = 0xF;  // write to the pushbutton interrupt mask register
                         // and set mask bits to 1

  while (1) {
    read_keyboard(&pressedKey);
    int keytracker = 0;
    // Here you can add your logic based on the pressed key
    // For example:
    switch (key) {
      case 0x1C:  // Key A
        draw_card(1, cards[0]);
        keytracker = 1;
        keypressed = true;
        break;
      case 0x1B:  // Key S
        draw_card(2, cards[1]);
        keytracker = 2;
        keypressed = true;
        break;
      case 0x23:  // Key D
        draw_card(3, cards[2]);
        keytracker = 3;
        keypressed = true;
        break;
      case 0x2B:  // Key F
        draw_card(4, cards[3]);
        keytracker = 4;
        keypressed = true;
        break;
      case 0x34:  // Key G
        draw_card(5, cards[4]);
        keytracker = 5;
        keypressed = true;
        break;
      case 0x33:  // Key H
        draw_card(6, cards[5]);
        keytracker = 6;
        keypressed = true;
        break;
      case 0x3B:  // Key J
        draw_card(7, cards[6]);
        keytracker = 7;
        keypressed = true;
        break;
      case 0x42:  // Key K
        draw_card(8, cards[7]);
        keytracker = 8;
        keypressed = true;
        break;
      default:
        for (int i = 1; i < 9; i++) {
          draw_card(i, BACK);
        }
        break;
    }
    if (key > 0 && keypressed == true) {
      if (k == 1) {
        first_card = cards[keytracker - 1];
        first_pos = keytracker;
        k++;
      } else if (k == 2) {
        second_card = cards[keytracker - 1];
        second_pos = keytracker;
      }
    }
    // compare the two selected cards;
    if (k == 2) {
      if (first_card == second_card) {
        // match!!
        // show on LEDs & print a green dot onto the card
        *leds_ptr = 1;
        draw_card(first_pos, MATCHED_CARD);
        draw_card(second_pos, MATCHED_CARD);
        match_counter++;
      } else {
        // not a match loser
        // flip cards back over
        draw_card(first_pos, BACK);
        draw_card(second_pos, BACK);
      }
    }
  }
  while (1)
    ;  // idle here until game done

  return 0;
}

